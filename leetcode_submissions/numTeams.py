    def numTeams_invalid(self, rating: List[int]) -> int:
        import numpy as np

        team_count = 0
        rating = np.array(rating)
        length = rating.shape[0]
        for i in range(length - 2):
            greater = rating[i + 1 :][rating[i + 1 :] > rating[i]]
            for j in greater:
                index_j = np.where(rating == j)[0][0]
                more_greater = rating[index_j + 1 :][
                    rating[index_j + 1 :] > rating[index_j]
                ]
                team_count += more_greater.shape[0]
            less = rating[i + 1 :][rating[i + 1 :] < rating[i]]
            for j in less:
                index_j = np.where(rating == j)[0][0]
                more_less = rating[index_j + 1 :][
                    rating[index_j + 1 :] < rating[index_j]
                ]
                team_count += more_less.shape[0]
        return team_count

    def numTeams(self, rating: List[int]) -> int:
        import numpy as np

        team_counts = 0
        rating = np.array(rating)
        for i in range((rating.shape[0])):
            first = rating[:i][rating[:i] > rating[i]]
            second = rating[i + 1 :][rating[i + 1 :] < rating[i]]
            team_counts += first.shape[0] * second.shape[0]
            third = rating[:i][rating[:i] < rating[i]]
            fourth = rating[i + 1 :][rating[i + 1 :] > rating[i]]
            team_counts += third.shape[0] * fourth.shape[0]
        return team_counts


