from typing import List


class Solution:
    def numTeams_old(self, rating: List[int]) -> int:
        length = len(rating)
        team_count = 0
        for i in range(0, length - 2):
            for j in range(i, length - 1):
                for k in range(j, length):
                    first = rating[i]
                    second = rating[j]
                    third = rating[k]
                    if first < second < third:
                        team_count += 1
                    elif first > second > third:
                        team_count += 1
        return team_count

